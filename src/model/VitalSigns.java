public class VitalSigns {
    private int breatheRate;
    private int bloodPreasure;
    private int heartRate;
    private int sugar;

    public int getBreatheRate() {
        return breatheRate;
    }

    public void setBreatheRate(int breatheRate) {
        this.breatheRate = breatheRate;
    }

    public int getBloodPreasure() {
        return bloodPreasure;
    }

    public void setBloodPreasure(int bloodPreasure) {
        this.bloodPreasure = bloodPreasure;
    }

    public int getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(int heartRate) {
        this.heartRate = heartRate;
    }

    public int getSugar() {
        return sugar;
    }

    public void setSugar(int sugar) {
        this.sugar = sugar;
    }
    
    
}
